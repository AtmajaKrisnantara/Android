package com.example.addressbook;

public class Contact {

    public String name;
    public String nickname;
    public String phone;
    public String email;
    public String address;

    public Contact(String name, String nickname, String phone, String email, String address) {
        this.name = name;
        this.nickname = nickname;
        this.phone = phone;
        this.email = email;
        this.address = address;
    }
}
